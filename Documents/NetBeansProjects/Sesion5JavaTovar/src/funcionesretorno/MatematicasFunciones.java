
package funcionesretorno;

public class MatematicasFunciones {//inicia clase
    //funcion suma
    public static int suma(int num1,int num2){//inicia metodo de tipo retorno entero
        return num1+num2;
    }//termina metodo de tipo retorno entero
    //funcion resta
    public static int resta(int num1,int num2){//inicia metodo de tipo retorno entero
        return num1-num2;
    }//termina metodo de tipo retorno entero
    //funcion resta
    public static int divi(int num1,int num2){//inicia metodo de tipo retorno entero
        return num1/num2;
    }//termina metodo de tipo retorno entero
    //funcion resta
    public static int multi(int num1,int num2){//inicia metodo de tipo retorno entero
        return num1*num2;
    }//termina metodo de tipo retorno entero
}//termina clase
